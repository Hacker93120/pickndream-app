  static List<String> searchCities(String query) {
    if (query.isEmpty) return cities.take(10).toList();
    
    final lowercaseQuery = query.toLowerCase();
    return cities
        .where((city) => city.toLowerCase().contains(lowercaseQuery))
        .take(10)
        .toList();
